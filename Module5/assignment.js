// Assignment =
// Combining assignment opertor with mathematical operator += -= *= /= %= **=
let val1 = 6;
val1+=3;
console.log(`Val 1: ${val1}`);

let val2 = 2;
val2 **= 3;
console.log(`Val 2: ${val2}`);

// Assignment with increment and decrement operators
let val3 = 10;
let val4 = val3++;
console.log(`Val 3: ${val3}, Val 4: ${val4}`);

let val5 = 10;
let val6 = --val5;
console.log(`Val 5: ${val5}, Val 6: ${val6}`);