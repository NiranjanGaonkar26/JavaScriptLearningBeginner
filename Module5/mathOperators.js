// Addition
let val1 = 3+2;
console.log(`Val 1: ${val1}`);
let val2 = 3 + -1;                      //Such a syntax is unique to JS
console.log(`Val 2: ${val2}`);

//Subtraction
let val3 = 3 - 1;
console.log(`Val 3: ${val3}`);
let val4 = 1 - -1;                      //Again this syntax is unique to JS
console.log(`Val 4: ${val4}`);

//Division
let val5 = 3/1;
console.log(`Val 5: ${val5}`);
let val6 = 17/3;                        // This gives a floating point quotient
console.log(`Val 6: ${val6}`);
let val7 = Math.floor(17/3);            //So if you need an integer quotient, then use the Math.floor() function
console.log(`Val 7: ${val7}`);

//Multiplication
let val8 = 2*3;
console.log(`Val 8: ${val8}`);

//Remainder
let val9 = 3%2;
console.log(`Val 9: ${val9}`);
let val10 = -10%7;
console.log(`Val 10: ${val10}`);

//Exponentitation
let val11 = 2 ** 3;
console.log(`Val 11: ${val11}`);
let val12 = 2 ** -1;
console.log(`Val 12: ${val12}`);

// Increment and decerement ---- pre and post increment/ decrement works as usual.
let val13 = 1;
console.log(`Val 13: ${val13++}`);
console.log(`Val 13: ${val13}`);

let val14 = 1;
console.log(`Val 14: ${val14--}`);
console.log(`Val 14: ${val14}`);

let val15 = 1;
console.log(`Val 15: ${++val15}`);

let val16 = 1;
console.log(`Val 16: ${--val16}`);



