// Collection: A datastructure which holds multiple items
// 3 primary collection types in JS: array, map, set
// Array: ordered collection of items. It has a `length` property and methods for manipulating the list. ORDERED Dataset
// Map: Key - value pair similar to a JS object. But here you can use ANY DATA TYPE as your key, whereas in an object a key is ALWAYS A STRING type. It has a `size` property and methods for manipulating the collection.
// Set: collection type which allows only UNIQUE set of elements. It has a `size` property and methods for manipulating the set.

// These collection types are iterable.